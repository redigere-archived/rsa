    PARTITA_IVA VARCHAR2(16) NOT NULL,
    NOME        VARCHAR2(64) NOT NULL,
    CONSTRAINT PK_FORNISCE PRIMARY KEY (PARTITA_IVA, NOME),
    CONSTRAINT FK_FORNISCE_DITTA_RIST
        FOREIGN KEY (PARTITA_IVA) REFERENCES DITTA_RISTORANTE(PARTITA_IVA),
    CONSTRAINT FK_FORNISCE_DIETA
        FOREIGN KEY (NOME) REFERENCES DIETA(NOME)
);

-- Associativa: una ditta di pulizie pulisce una camera
CREATE TABLE PULISCE (
