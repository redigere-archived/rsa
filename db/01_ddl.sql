-- Associativa: una ditta di ristorazione fornisce una dieta
-- RISTRUTTURATA: in precedenza legava DITTA_RISTORANTE↔CAMERA con un campo NOME libero.
--   Ora segue il modello ER: DITTA_RISTORANTE↔DIETA (entrambe FK)
