CREATE OR REPLACE FUNCTION CONTEGGIO_VISITE_RESIDENTE(CF_RESIDENTE IN VARCHAR2, DATA_INIZIO IN DATE, DATA_FINE IN DATE)
RETURN NUMBER IS
  NUMERO_VISITE NUMBER;
BEGIN
  SELECT COUNT(*) INTO NUMERO_VISITE
  FROM VISITA
  WHERE VISITA.CODICE_FISCALE_RESIDENTE = CF_RESIDENTE
    AND VISITA.GIORNO_VISITA BETWEEN DATA_INIZIO AND DATA_FINE;
  RETURN NUMERO_VISITE;
END;
