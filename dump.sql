    v_ammesso NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_ammesso FROM RESIDENTE WHERE CODICE_FISCALE_RESIDENTE = p_cf_residente;
    IF v_ammesso > 0 THEN
        RETURN 1;
    ELSE
        RETURN 0;
    END IF;
END;
